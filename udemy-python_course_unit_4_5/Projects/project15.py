grades = int(input("Enter Your Grades: "))

if (grades > 90):
    print("You got an A grade 😃!!")
elif (grades > 80):
    print("You got a B grade, not bad 🙂!!")
elif (grades > 70):
    print("You got a C grade, its ok 😐")
elif (grades > 60):
    print("You got a D grade, you need to improve 😕")
else:
    print("You got an F, that is not good 😠")

